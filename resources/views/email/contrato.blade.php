    <h1>CONTRATO DE LÍNEA DE CRÉDITO</h1>

    <div class="section">
        <p>El usuario (el “Cliente”), cuyos datos fueron proporcionados al momento de registrarse a la plataforma BLUPY (la “Plataforma”), y <strong>MI CREDITO S.A.</strong> con domicilio en Acceso Sur e/ Juana P. Carrillo, Nro 427, Ñemby, Paraguay y con RUC Nro. 80119067-3 (“BLUPY”) celebran el presente contrato de línea de crédito de consumo (el “Contrato”), que se regirá por las siguientes cláusulas y condiciones:</p>
    </div>

    <div class="section">
        <h2>De la línea de crédito:</h2>
        <p>Por el presente Contrato, BLUPY otorga una línea de crédito no comprometida al Cliente para compra de productos en la cadena de farmacias PUNTO FARMA, la cual estará disponible dentro del plazo de 48 horas hábiles posteriores de: (i) la aceptación del presente Contrato por parte del Cliente; (ii) la aceptación de los términos específicos de la línea de crédito enviados al Cliente por correo electrónico, y a través de la Plataforma, y (iii) firmando y entregando los términos específicos del Contrato en uno de los locales de la cadena de farmacias PUNTO FARMA, junto con la presentación de su cédula de identidad.</p>
        <p>Una vez que la línea de crédito quede activa, el Cliente recibirá un mensaje de texto y podrá visualizarse dentro de la plataforma BLUPY en la sección: “MI LÍNEA DE CRÉDITO”. Los datos disponibles para visualizar serán, entre otros, el monto de la línea de crédito asignada, el saldo disponible, monto de la deuda generada (si hubiere), tasa de interés aplicada, fecha de vencimiento para efectuar el pago, historial de compras y pagos.</p>
        <p>El Cliente acepta y reconoce que la línea de crédito es no comprometida, en el entendimiento que BLUPY podrá dejarla sin efecto en cualquier momento, a su entera discreción. En estos supuestos, BLUPY estará exonerada de cualquier responsabilidad. Este monto de la línea de crédito podrá ser modificado en cualquier momento, previa comunicación por parte de BLUPY al Cliente, por cualquier medio escrito, incluso electrónico.</p>
        <p>La línea de crédito podrá ser utilizada exclusivamente para la compra de productos en la cadena de farmacias PUNTO FARMA.</p>
        <p>En caso de que el Cliente no haga uso de la línea de crédito dentro del plazo de tres (3) meses, posteriores a la aprobación de la línea de crédito, la misma quedará sin efecto y se tendrá por rescindido de pleno derecho este Contrato, sin responsabilidad alguna a cargo de BLUPY. Producido este hecho, en el supuesto que el Cliente desee utilizar nuevamente la línea de crédito, deberá cumplir desde el inicio con todos los trámites y pasos dispuestos por BLUPY para el efecto.</p>
    </div>

    <div class="section">
        <h2>De los pagos:</h2>
        <p>El Cliente se obliga a pagar a BLUPY las sumas adeudadas por la utilización de la línea de crédito, en guaraníes, según los plazos indicados los términos específicos de la línea de crédito que fueron aceptados por el Cliente.</p>
        <p>El pago de los montos utilizados de la línea de crédito, los intereses devengados, y los demás conceptos, tales como gastos de cobranza, mantenimiento mensual, podrá realizarse en los locales de farmacia PUNTO FARMA, o a través de los canales digitales oficiales habilitados por BLUPY.</p>
    </div>

    <div class="section">
        <h2>De los intereses y la falta de pago:</h2>
        <p>Por la utilización de la línea de crédito, el Cliente estará obligado al pago de una tasa de interés compensatoria la cual se indicará en los términos específicos de la línea de crédito.</p>
        <p>En caso de incumplimiento o atraso en el pago de las sumas utilizadas dentro de la línea de crédito de conformidad con lo estipulado en los términos específicos de la línea de crédito, se producirá la mora del Cliente de manera automática y de pleno derecho, sin necesidad de protesto o ningún requerimiento judicial o extrajudicial por parte de BLUPY.</p>
        <p>El interés compensatorio se convierte, a partir de la mora, en interés moratorio y se cobrará una tasa equivalente a la tasa compensatoria. El interés moratorio será calculado sobre el saldo de la deuda vencida. Adicionalmente, BLUPY percibirá un interés punitorio, cuya tasa será equivalente al treinta por ciento (30%) de la tasa a percibir en concepto de interés moratorio. El interés punitorio será calculado sobre el saldo de la deuda vencida.</p>
        <p>El Cliente será responsable igualmente de abonar a BLUPY, cualquier costo relacionado con gestiones de cobro que se deban realizar una vez configurada la mora.</p>
        <p>Asimismo, el Cliente autoriza expresamente a BLUPY a informar sobre el incumplimiento de sus obligaciones de pago a las centrales de riesgo, burós de crédito u otras entidades públicas o privadas legalmente habilitadas para recibir dicha información, conforme a la legislación vigente en la República del Paraguay, incluyendo la Ley N° 6534/20 de Protección de Datos Personales Crediticios y demás normativas aplicables.</p>
    </div>

    <div class="section">
        <h2>Terminación del Contrato:</h2>
        <p>BLUPY podrá terminar el presente Contrato, y, por lo tanto, la línea de crédito, sin expresión de causa, en cualquier momento, previa notificación a la otra parte a través de una comunicación cursada de conformidad con lo estipulado en la cláusula Undécimo. En el caso de que queden montos pendientes de pago, el Cliente estará obligado a cancelarlos en los plazos estipulados en las condiciones específicas.</p>
        <p>Sin perjuicio de lo establecido, BLUPY podrá terminar este Contrato de forma inmediata, de pleno derecho, en caso de incumplimiento de los términos de este Contrato por parte del Cliente, incluyendo cualquier incumplimiento de pago, hecho que le será comunicado al Cliente a través de una comunicación cursada de conformidad con lo estipulado en la cláusula Octavo. En el caso que queden montos pendientes de pago, el Cliente estará obligado a cancelar la totalidad de lo adeudado a BLUPY a los cinco (5) días a partir de la fecha de terminación.</p>
    </div>

    <div class="section">
        <h2>De la información suministrada:</h2>
        <p>El Cliente declara que toda la información suministrada en el marco del presente Contrato, así como previamente a través de la Plataforma, es veraz, correcta y exacta, y se obliga a notificar a BLUPY respecto a cualquier modificación. En caso de falsedad o incumplimiento del deber de informar a BLUPY, ésta podrá proceder a la suspensión o a la cancelación definitiva de la línea de crédito, hecho que le será comunicado al Cliente oportunamente a través de una comunicación cursada de conformidad con lo estipulado en la cláusula Undécimo.</p>
    </div>

    <div class="section">
