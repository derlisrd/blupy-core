        <p>El Cliente reconoce y acepta que, la utilización y/o validación de su contraseña, código de acceso, clave personal, código transaccional, token y/o datos biométricos, siendo esta enunciación meramente ilustrativa y no limitativa, serán válidos y vinculantes a su persona, como si fueran realizados por el Cliente. Por tanto, exonera a BLUPY de cualquier tipo de responsabilidad derivada de hechos de hurto, robo o mala utilización por parte de terceros de sus contraseñas, códigos, datos personales y todo elemento que lo pudiera individualizar.</p>
    </div>

