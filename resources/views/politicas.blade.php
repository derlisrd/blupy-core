    <h1>Política de Privacidad de BLUPY</h1>
    <p>Por favor, lea con atención este apartado para estar informado sobre nuestras políticas de privacidad.
        Esta Política de Privacidad describe cómo MI Crédito S.A. ("nosotros", "nuestro" o "la Compañía") recopila, utiliza y protege los datos personales de los usuarios de su producto BLUPY ("BLUPY" o "la Aplicación"). Al registrarte y utilizar BLUPY, aceptas las prácticas descritas en esta política.</p>

    <h1>1. Recopilación de Información Personal</h1>
    <p>Recopilamos la siguiente información personal para poder ofrecerte y gestionar el servicio de crédito BLUPY:</p>
    <ul>
        <li><strong>Datos de registro:</strong> Nombre completo, número de cédula de identidad, fecha de nacimiento, número de teléfono y dirección de correo electrónico.</li>
        <li><strong>Información de validación:</strong> Fotografía de tu cédula de identidad, utilizada para verificar la exactitud de los datos de registro.</li>
        <li><strong>Información financiera y crediticia:</strong> Datos sobre tus ingresos mensuales, antigüedad laboral e historial crediticio, obtenido a través de la consulta en bases de datos como Informconf.</li>
        <li><strong>Datos de transacciones:</strong> Información sobre tus compras en sucursales de Punto Farma y en la web, incluyendo monto, fecha y lugar de la transacción.</li>
    </ul>

    <h1>2. Uso de la Información</h1>
    <p>Utilizamos la información recopilada para los siguientes propósitos:</p>
    <ul>
        <li><strong>Gestión del crédito:</strong> Para evaluar tu perfil crediticio y determinar si calificas para una línea BLUPY Digital.</li>
        <li><strong>Procesamiento de transacciones:</strong> Para autorizar y registrar tus compras realizadas a través de la aplicación.</li>
        <li><strong>Comunicación:</strong> Para enviarte notificaciones sobre tu cuenta, estado de la solicitud, saldos, fechas de cierre y vencimiento, así como información sobre productos, servicios y promociones (como los descuentos de los miércoles).</li>
        <li><strong>Mejora del servicio:</strong> Para analizar el uso de la aplicación y mejorar la experiencia del usuario, así como para desarrollar nuevos productos y servicios.</li>
        <li><strong>Prevención de fraudes:</strong> Para proteger tu cuenta y la Compañía de actividades fraudulentas o no autorizadas.</li>
        <li><strong>Cobranza:</strong> En caso de mora, para gestionar la cobranza de las deudas pendientes.</li>
    </ul>

    <h1>3. Protección de la Información</h1>
    <p>La seguridad de tu información personal es nuestra prioridad. Implementamos medidas de seguridad técnicas y organizativas para proteger tus datos contra el acceso no autorizado, la alteración, la divulgación o la destrucción.
        Mantenemos tu información en servidores seguros y solo el personal autorizado tiene acceso a ella.</p>

    <h1>4. Compartir Información con Terceros</h1>
    <p>No vendemos, alquilamos ni cedemos tu información personal a terceros para fines de marketing o publicidad.
        Podemos compartir tu información solo en los siguientes casos y con las siguientes entidades:</p>
    <ul>
        <li><strong>Punto Farma:</strong> Compartimos la información necesaria para procesar tus transacciones y ofrecer descuentos especiales.</li>
        <li><strong>Proveedores de servicios:</strong> Podemos utilizar servicios de terceros para el procesamiento de pagos, la validación de datos (como la verificación de la cédula) y la evaluación crediticia (como Informconf). Estos terceros están obligados a proteger tu información de manera segura y confidencial.</li>
        <li><strong>Obligaciones legales:</strong> Podemos divulgar tu información si estamos obligados por ley o si es necesario para proteger nuestros derechos, propiedad o seguridad, o los de otros.</li>
    </ul>

    <h1>5. Tus Derechos</h1>
    <p>Como usuario de BLUPY, tienes el derecho de acceder, corregir y actualizar tus datos personales.
        Si deseas ejercer estos derechos o tienes alguna pregunta sobre nuestra Política de Privacidad, puedes contactarnos a través de los canales de soporte de la aplicación o enviando un correo a <a href="mailto:atencionalcliente@blupy.com.py">atencionalcliente@blupy.com.py</a>.</p>

    <h1>6. Cambios en la Política de Privacidad</h1>
    <p>Nos reservamos el derecho de modificar esta Política de Privacidad en cualquier momento.
        Te notificaremos sobre cualquier cambio sustancial a través de la aplicación o por correo electrónico.
        El uso continuado de BLUPY después de la publicación de los cambios constituirá tu aceptación de la nueva política.</p>
